package com.basics;

public class HelloWorld_1 {
	
	public static void main(String[] args) {
		System.out.println("For-loop");
		for(int i=1;i<=10;i++) {
			System.out.println("Hello World:"+i);
		}
		System.out.println("---------------------------------------");
		System.out.println("While loop");
		
		int i=1;
		while(i<11) {
			System.out.println("Hello-World:"+i);
			i++;
		}
	}

}
